export const calculateCengage = (courses: string[]) => {
  const numberOfCourses = courses.filter((course) =>
    cengage.includes(course)
  ).length;
  if (numberOfCourses === 0) return 0;
  // $50 for 1 course, $100 for 2 courses, $120 for 3 or more courses
  if (numberOfCourses === 1) return 50;
  if (numberOfCourses === 2) return 100;
  return 120; // max 120
};
